const text = {
  decode(value) {
    return String(value || "")
      .replace(/\\u\{([0-9a-fA-F]+)\}/g, (match, point) =>
        String.fromCodePoint(parseInt(point, 16)),
      )
      .replace(/\\u([0-9a-fA-F]{4})/g, (match, point) =>
        String.fromCodePoint(parseInt(point, 16)),
      );
  },
  normalize(value) {
    return [...String(value || "")]
      .filter((symbol) => symbol.codePointAt(0).toString(16) !== "fe0f")
      .join("");
  },
  escape(value) {
    return String(value || "")
      .replace(/&/g, "&amp;")
      .replace(/</g, "&lt;")
      .replace(/>/g, "&gt;")
      .replace(/"/g, "&quot;");
  },
};
const registry = {
  list() {
    return Object.values(data);
  },
  emoji() {
    return registry.list().reduce(
      (result, item) => ({
        ...result,
        [item.emoji]: item,
      }),
      {},
    );
  },
  code() {
    return registry.list().reduce(
      (result, item) => ({
        ...result,
        [item.code]: item,
      }),
      {},
    );
  },
  clean() {
    return registry.list().reduce(
      (result, item) => ({
        ...result,
        [text.normalize(item.emoji)]: item,
      }),
      {},
    );
  },
  get(value) {
    const current = String(value || "");
    const name = alias[current] || current;
    const decoded = text.decode(current);
    return (
      data[name] ||
      registry.emoji()[decoded] ||
      registry.code()[current] ||
      registry.clean()[text.normalize(decoded)] ||
      null
    );
  },
  symbol(value, fallback = "") {
    return registry.get(value)?.emoji || fallback;
  },
  name(value, fallback = null) {
    return registry.get(value)?.iconify || fallback;
  },
};
const url = {
  fluent(name, size = 20) {
    return `https://raw.githubusercontent.com/microsoft/fluentui-system-icons/main/assets/${name}/SVG/ic_fluent_${name.toLowerCase().replaceAll(" ", "_")}_${size}_regular.svg`;
  },
  noto(value, code) {
    const symbols = code(value)
      .split("-")
      .map((part) => `u${part.padStart(4, "0")}`)
      .join("_");
    return `https://cdn.jsdelivr.net/gh/googlefonts/noto-emoji@main/svg/emoji_${symbols}.svg`;
  },
  twemoji(value, code) {
    return `https://cdn.jsdelivr.net/gh/twitter/twemoji@14.0.2/assets/svg/${code(value)}.svg`;
  },
};
const emojis = {
  pack: "fluent",
  unsupportedFluent: {},
  icons() {
    return registry.list().reduce(
      (result, item) => ({
        ...result,
        [item.emoji]: item.iconify,
      }),
      {},
    );
  },
  normalize(value) {
    return text.normalize(value);
  },
  namespace() {
    return {
      fluent: "fluent-emoji-flat",
      noto: "noto",
    }[emojis.pack];
  },
  name(value) {
    return registry.name(value);
  },
  emoji(value) {
    return registry.symbol(value, String(value || ""));
  },
  code(value) {
    const current = emojis.emoji(value);
    if (current === "#️⃣" || current === "#️") return "23-20e3";
    return [...current]
      .map((symbol) => symbol.codePointAt(0).toString(16))
      .filter((symbol) => symbol !== "fe0f")
      .join("-");
  },
  url(value) {
    const current = emojis.emoji(value);
    if (emojis.pack === "native") return null;
    if (emojis.pack === "twemoji") return icon.url.twemoji(current);
    if (emojis.pack === "noto") return icon.url.noto(current);
    if (emojis.unsupportedFluent[current]) return null;
    const namespace = emojis.namespace();
    const name = emojis.name(current);
    if (!namespace || !name) return null;
    return `https://api.iconify.design/${namespace}:${name}.svg`;
  },
  image(value) {
    const current = emojis.emoji(value);
    const source = emojis.url(current);
    if (!source) return current;
    const onerror = ` onerror="this.onerror=null;this.closest('.emoji').outerHTML='${current}'"`;
    return `<span class="emoji" data-emoji="${current}" style="width:1em;height:1em;display:inline-block;vertical-align:-0.12em;"><img alt="${current}" src="${source}"${onerror} style="width:100%;height:100%;display:block;"></span>`;
  },
  list() {
    return registry
      .list()
      .map((item) => item.emoji)
      .sort((left, right) => right.length - left.length);
  },
  replace(value) {
    return emojis
      .list()
      .reduce(
        (string, item) => string.split(item).join(emojis.image(item)),
        String(value || ""),
      );
  },
  html(value) {
    const current = String(value || "");
    if (emojis.pack === "native") return registry.symbol(current, current);
    if (registry.get(current)) return emojis.image(current);
    return emojis.replace(current);
  },
};
